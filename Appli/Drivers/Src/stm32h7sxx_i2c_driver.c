/*@fn				- I2C_ClearCR2TransferFields
 *@note				- CR2 Transfer Fields Clearing Function
 */
static void I2C_ClearCR2TransferFields(I2C_RegDef_t *pI2Cx){
    pI2Cx->CR2 &= ~((0x3FFU << I2C_CR2_SADD)    |
                    (1U    << I2C_CR2_RD_WRN)  |
                    (1U    << I2C_CR2_ADD10)   |
                    (0xFFU << I2C_CR2_NBYTES)  |
                    (1U    << I2C_CR2_RELOAD)  |
                    (1U    << I2C_CR2_AUTOEND));
}

/*@fn				- I2C_CheckAndClearErrors
 *@note				- I2C Error Check-Clear
 */
static uint8_t I2C_CheckAndClearErrors(I2C_RegDef_t *pI2Cx){
    if (I2C_GetFlagStatus(pI2Cx, I2C_FLAG_NACKF) == FLAG_SET){
        pI2Cx->ICR |= (1U << I2C_ICR_NACKCF);
        return I2C_ERROR_NACK;
    }

    if (I2C_GetFlagStatus(pI2Cx, I2C_FLAG_BERR) == FLAG_SET){
        pI2Cx->ICR |= (1U << I2C_ICR_BERRCF);
        return I2C_ERROR_BERR;
    }

    if (I2C_GetFlagStatus(pI2Cx, I2C_FLAG_ARLO) == FLAG_SET){
        pI2Cx->ICR |= (1U << I2C_ICR_ARLOCF);
        return I2C_ERROR_ARLO;
    }

    if (I2C_GetFlagStatus(pI2Cx, I2C_FLAG_OVR) == FLAG_SET){
        pI2Cx->ICR |= (1U << I2C_ICR_OVRCF);
        return I2C_ERROR_OVR;
    }

    return I2C_OK;
}

/*@fn				- I2C_WaitOnFlagUntilTimeout
 *@note				- I2C Timeout Function
 */
static uint8_t I2C_WaitOnFlagUntilTimeout(I2C_RegDef_t *pI2Cx, uint32_t FlagName, uint8_t DesiredStatus, uint32_t Timeout){
    uint8_t error_status;

    while (I2C_GetFlagStatus(pI2Cx, FlagName) != DesiredStatus){
        error_status = I2C_CheckAndClearErrors(pI2Cx);
        if (error_status != I2C_OK){
            return error_status;
        }

        if (Timeout-- == 0U){
            return I2C_ERROR_TIMEOUT;
        }
    }

    return I2C_OK;
}

/*@fn				- I2C_ConfigTransfer
 *@note				- I2C Configuration Transfer Function
 *					  For Direction selection, see @I2C_TRANSFER_DIRECTIONS macros.
 */
static void I2C_ConfigTransfer(I2C_RegDef_t *pI2Cx, uint8_t SlaveAddr, uint8_t Direction, uint32_t NBytes, uint8_t Reload, uint8_t AutoEnd) {
    I2C_ClearCR2TransferFields(pI2Cx);

    // 7-bit slave address (e.g: 0x3C, 0x68): Hardware expects it in SADD[7:1].
    // No 10-bit address support (05.06.2026)
    pI2Cx->CR2 |= ((uint32_t)(SlaveAddr & 0x7FU) << 1U);

    if (Direction == I2C_DIR_READ){
        pI2Cx->CR2 |= (1U << I2C_CR2_RD_WRN);
    } else if (Direction == I2C_DIR_WRITE){
        pI2Cx->CR2 &= ~(1U << I2C_CR2_RD_WRN);
    } else{
    	//Invalid config
    	return;
    }

    pI2Cx->CR2 |= ((NBytes & 0xFFU) << I2C_CR2_NBYTES);

    if (Reload == ENABLE){
        pI2Cx->CR2 |= (1U << I2C_CR2_RELOAD);
    }

    if (AutoEnd == ENABLE){
        pI2Cx->CR2 |= (1U << I2C_CR2_AUTOEND);
    }
}
